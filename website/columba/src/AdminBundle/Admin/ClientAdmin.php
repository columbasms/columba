    /**
     * @param ShowMapper $showMapper
     */
    protected function configureShowFields(ShowMapper $showMapper)
    {
        $showMapper
            ->add('id')
            ->add('phoneNumber')
            ->add('firstName')
            ->add('lastName')
            ->add('email')
            ->add('enabled')
            ->add('lastLogin')
